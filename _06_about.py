# pages/_06_about.py
import streamlit as st
import sqlite3
import pandas as pd
from config import DB_PATH


def render_about():
    """страница с описанием системы, источников данных и методологии"""
    st.header("О системе")

    # описание системы
    st.markdown("""
    **RiskAnalyzer** — информационно-аналитическая система оценки рисков расторжения
    государственных строительных контрактов, разработанная в рамках магистерской диссертации.

    Система предназначена для специалистов в сфере государственных закупок и позволяет:
    - оценивать вероятность расторжения контракта до его заключения
    - анализировать надёжность поставщиков по истории исполнения
    - выявлять региональные и отраслевые закономерности в расторжениях
    - сравнивать качество различных моделей машинного обучения
    """)

    st.markdown("---")

    # статистика базы данных
    st.subheader("Данные")
    try:
        conn = sqlite3.connect(DB_PATH)
        stats = pd.read_sql_query("""
            SELECT
                COUNT(*)                     AS total_contracts,
                SUM(is_terminated)           AS terminated,
                COUNT(DISTINCT region)       AS regions,
                COUNT(DISTINCT customer_inn) AS customers,
                MIN(exe_start)               AS date_from,
                MAX(exe_start)               AS date_to
            FROM contracts WHERE stage IN ('ET', 'EC')
        """, conn).iloc[0]

        suppliers_cnt = pd.read_sql_query(
            "SELECT COUNT(DISTINCT supplier_inn) AS cnt FROM contract_suppliers", conn
        ).iloc[0]['cnt']

        procedures_cnt = pd.read_sql_query(
            "SELECT COUNT(*) AS cnt FROM procedures_flat", conn
        ).iloc[0]['cnt']

        conn.close()

        c1, c2, c3 = st.columns(3)
        with c1:
            st.markdown(f"""
            <div class="metric-card">
                <h3>Контрактов в базе</h3>
                <h2>{int(stats['total_contracts']):,}</h2>
                <div class="metric-sub">из них расторгнуто: {int(stats['terminated']):,}</div>
            </div>
            """, unsafe_allow_html=True)
        with c2:
            st.markdown(f"""
            <div class="metric-card">
                <h3>Поставщиков</h3>
                <h2>{int(suppliers_cnt):,}</h2>
                <div class="metric-sub">уникальных ИНН</div>
            </div>
            """, unsafe_allow_html=True)
        with c3:
            st.markdown(f"""
            <div class="metric-card">
                <h3>Процедур исполнения</h3>
                <h2>{int(procedures_cnt):,}</h2>
                <div class="metric-sub">штрафы, расторжения, отчёты</div>
            </div>
            """, unsafe_allow_html=True)

        st.markdown(f"""
        **Период данных:** {str(stats['date_from'])[:10]} — {str(stats['date_to'])[:10]}
        **Охват:** {int(stats['regions'])} регионов РФ, {int(stats['customers']):,} заказчиков
        **Источник:** Единая информационная система в сфере закупок (ЕИС) через API ГосПлан
        **Классификатор:** строительство зданий (ОКПД2 41.x), инфраструктура (42.x), спецработы (43.x)
        """)

    except Exception as e:
        st.error(f"не удалось загрузить статистику: {e}")

    st.markdown("---")

    # методология
    st.subheader("Методология")

    st.markdown("""
    **Постановка задачи**

    Задача сформулирована как бинарная классификация: по параметрам контракта и истории
    поставщика предсказать, будет ли контракт расторгнут (ET) или успешно завершён (EC).
    Целевая переменная: `is_terminated` ∈ {0, 1}.

    **Признаки модели**

    Признаки разделены на три группы:
    - *Параметры контракта* — логарифм цены, длительность, тип работ по ОКПД2, регион, число поставщиков
    - *Процедуры исполнения* — количество штрафов, их сумма и тяжесть, активность процедур
    - *История поставщика* — общее число контрактов, средняя цена, число регионов работы

    **Предотвращение утечки данных (data leakage)**

    Признак `has_termination_doc` удаляется перед обучением — он появляется только после
    расторжения и недоступен на момент прогноза. Признак `supplier_terminated_share`
    вычисляется по всей истории поставщика включая предсказываемый контракт — поэтому
    используется только в аналитике, но не в моделях.

    **Обработка дисбаланса классов**

    Доля расторжений ~22%. Для борьбы с дисбалансом:
    - логрег и деревья: `class_weight='balanced'`
    - XGBoost, LightGBM, CatBoost: `scale_pos_weight = N_negative / N_positive`

    **Оптимизируемая метрика: F1-score**

    F1 выбран как баланс между precision и recall. В контексте госзакупок recall важнее
    (лучше проверить лишний контракт, чем пропустить реальный риск расторжения).

    **Валидация**

    Разбивка train/test: 70/30, стратифицированная по целевой переменной.
    Подбор гиперпараметров: GridSearchCV с StratifiedKFold (5 фолдов).
    Базовая линия: DummyClassifier (strategy='most_frequent').
    """)

    st.markdown("---")

    # признак "регион"
    st.subheader("Примечание о признаке «Регион»")
    st.markdown("""
    Регион подаётся в модели как числовой код (77 — Москва, 78 — Санкт-Петербург и т.д.).
    Для ансамблевых моделей (деревья, RF, XGBoost, LightGBM, CatBoost) это не создаёт
    проблем — они разбивают по порогам и не предполагают линейного порядка.
    Для логистической регрессии числовое кодирование некорректно, однако в нашем случае
    коэффициент при признаке «регион» имеет малый вес, что подтверждается анализом
    важности признаков. При необходимости может быть применён target encoding.
    """)

    st.markdown("---")

    # ограничения
    st.subheader("Ограничения системы")
    st.markdown("""
    1. **Историческая база** — модели обучены на завершённых и расторгнутых контрактах.
       Контракты в стадии исполнения не включены в обучающую выборку.

    2. **Охват данных** — выборка ограничена строительной отраслью (ОКПД2 41–43).
       Для других отраслей модели требуют переобучения.

    3. **Регион как признак** — числовое кодирование региона не отражает
       географическую близость или социально-экономические характеристики.

    4. **Новые поставщики** — для поставщиков без истории прогноз менее надёжен:
       признаки истории поставщика заполняются нулями.

    5. **Не является юридическим инструментом** — результаты носят информационный
       характер и не могут служить основанием для отказа в заключении контракта.
    """)

    st.markdown("---")

    # технический стек
    st.subheader("Технический стек")
    c1, c2 = st.columns(2)
    with c1:
        st.markdown("""
        **Backend / ML**
        - Python 3.14
        - scikit-learn (Pipeline, GridSearchCV, StratifiedKFold)
        - XGBoost, LightGBM, CatBoost
        - SQLite (локальная БД), API ГосПлан (fz44)
        """)
    with c2:
        st.markdown("""
        **Frontend**
        - Streamlit
        - Plotly (интерактивные графики)
        - Pandas, NumPy
        """)